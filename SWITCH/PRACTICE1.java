package SWITCH;
public class PRACTICE1 {
    public static void main(String[] args) {

        int number = 22;

        switch (number){

            case 2:
                System.out.println("First");
                break;
            case 4:
                System.out.println("second");
                break;
            default:
                System.out.println("This is default");
            case 5:
                System.out.println("Third");
                break;
            case 28:
                System.out.println("Fourth");
                break;


        }

    }
}
