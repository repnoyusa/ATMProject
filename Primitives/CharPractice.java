package Primitives;

public class CharPractice {
    public static void main(String[] args) {


        char firstLetter='A';//value for a char data type needs to be in the single quotation mark ('A')

        System.out.println(firstLetter);

        char secondLetter='6';
        System.out.println(secondLetter);

        char thirdLetter='$';
        System.out.println(thirdLetter);


        char forthLetter=112;
        System.out.println(forthLetter);
        // char forthLetter1='66';// you can store only single character in single quotation mark for char

        char fithLetter=6969;
        System.out.println(fithLetter);

        char x=42;// if you assign a number for a char data type, it will print a symbol/character from ASCII table


        double d='?';// if you assign  a symbol/character from ASCII table
        //it will print the number /value for it
        System.out.println(d);

        int i='%';
        int b='2';

        System.out.println(b);


    }



}
